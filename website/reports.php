            <div>
                <div class="report-card-title">GDS Service Standard (UK Government Digital Service)</div>
                <div class="report-card-date">Government Digital Service, 2019 (updated regularly)</div>
            </div>
        </div>
        <div class="report-card-body">
            <p>The UK Government Digital Service Standard sets out 14 criteria that government services must meet to be delivered digitally. While Scottish public services follow SAtSD rather than the GDS standard as their primary framework, the GDS standard is a useful comparative reference that shares many of the same principles and adds specific technical and operational requirements.</p>
            <div class="report-findings">
                <h5>Key Findings / Key Points</h5>
                <ul>
                    <li>Understand users and their needs (Point 1) &mdash; requires user research, not assumed requirements</li>
                    <li>Solve a whole problem for users (Point 2) &mdash; end-to-end service design, not just digitising a form</li>
                    <li>Provide a joined up experience across all channels (Point 3) &mdash; phone, web, and in-person must be coherent</li>
                    <li>Make the service simple to use (Point 4) &mdash; WCAG 2.1 AA, plain language, inclusive design</li>
                    <li>Make sure everyone can use the service (Point 5) &mdash; digital inclusion as a design requirement, not an add-on</li>
                    <li>Have a plan for being offline (Point 11) &mdash; what happens when the portal is down</li>
                    <li>Measure performance (Point 10) &mdash; four key metrics: completion rate, user satisfaction, cost per transaction, uptake</li>
                </ul>
            </div>
            <div class="relevance-note">
                <span><?php echo icon('check-circle', 14); ?></span>
                <span><strong>Why this matters to me:</strong> I reference the GDS Service Standard to structure the tenant portal requirements and give technical partners a shared language. Points 5 (solve the whole problem for users) and Point 10 (define what success looks like) map directly to the KPIs and digital inclusion strategy in my delivery plan — and they are points I can cite by number when discussing design decisions.</span>
