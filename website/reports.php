                <div class="report-card-date">Audit Scotland, 2021</div>
            </div>
        </div>
        <div class="report-card-body">
            <p>Audit Scotland's 2021 review of digital transformation programmes across Scottish local authorities. Provides independent analysis of what has worked, what has failed, and what governance and management disciplines make the difference between successful and unsuccessful digital programmes.</p>
            <div class="report-findings">
                <h5>Key Findings</h5>
                <ul>
                    <li><strong>Unclear ownership</strong> is the single most common cause of digital programme failure in Scottish councils</li>
                    <li><strong>Benefits not baselined:</strong> Most programmes could not demonstrate measurable improvement because they failed to measure the &ldquo;before&rdquo; state</li>
                    <li><strong>Change management as afterthought:</strong> Technology deployed without adequate staff engagement consistently fails to deliver intended productivity gains</li>
                    <li><strong>Procurement delays</strong> are the most common cause of timeline slippage in council digital programmes</li>
                    <li>Councils that invested in digital skills (user research, agile delivery) before starting major programmes achieved significantly better outcomes</li>
                    <li>Resident/tenant involvement in design correlates strongly with better adoption rates and higher satisfaction</li>
                </ul>
