    <div class="section-header" style="margin-top:2rem;">
        <h2>Digital Transformation Timeline</h2>
        <p>Key milestones in ERC's digital journey, in chronological order.</p>
    </div>

    <div class="timeline">

        <!-- 2018-2019 -->
        <div class="timeline-item">
            <div class="timeline-date">2018 &ndash; 2019</div>
            <div class="timeline-title">Early Digital Foundations</div>
            <div class="timeline-body">
                <p>ERC begins structured exploration of digital service delivery, moving beyond basic website functionality towards channel shift and customer experience improvement. Early CRM implementations put in place in selected service areas. The council adopts a programme-based approach to change management, planting the seeds of what becomes the MAP programme model.</p>
                <p>Key focus during this period: identifying which services are suitable for digital channel shift; early workforce digital skills assessment; procurement landscape review for ICT infrastructure modernisation.</p>
                <span class="badge badge-grey">Foundation</span>
            </div>
        </div>

        <!-- July 2020 -->
        <div class="timeline-item">
            <div class="timeline-date">July 2020</div>
            <div class="timeline-title">New Council Website &amp; CXM Platform Selection</div>
            <div class="timeline-body">
                <p>East Renfrewshire Council launches a redesigned council website, significantly improving the user experience for residents accessing council services online. More significantly, <strong>Goss CXM (Customer Experience Management platform)</strong> is selected as the council's strategic customer service technology. This decision underpins the subsequent migration of paper-based and legacy digital forms into the new CXM system, creating a unified customer service layer across all departments.</p>
                <p>The Goss selection is strategically significant for the Housing Digital Transformation: the tenant repairs portal will integrate with Goss CXM to create a single view of tenant interactions, avoiding the creation of yet another silo.</p>
                <span class="badge badge-blue">Infrastructure</span>
                <span class="badge badge-teal" style="margin-left:0.4rem;">CXM Platform</span>
            </div>
        </div>

        <!-- 2020-2021 -->
        <div class="timeline-item">
            <div class="timeline-date">2020 &ndash; 2021</div>
            <div class="timeline-title">COVID Accelerator: Microsoft 365 Adoption at Scale</div>
            <div class="timeline-body">
                <p>The COVID-19 pandemic forces a rapid and comprehensive adoption of remote working technology across ERC's 2,000+ staff. <strong>Microsoft 365</strong> becomes the council's primary productivity platform virtually overnight. Teams replaces in-person meetings as the primary communication and collaboration tool. SharePoint begins replacing shared drives for document management.</p>
                <p>What would have taken years of planning and phased rollout is compressed into weeks of necessity-driven implementation. The result is a workforce that is genuinely comfortable with digital collaboration tools &mdash; a significant cultural shift that underpins all subsequent digital transformation work.</p>
                <p>From a Housing Digital Transformation perspective, this M365 investment is directly relevant: Power BI (for KPI dashboards), Power Automate (for workflow automation), and Teams (for delivery team collaboration) are all available as part of the existing M365 subscription.</p>
                <span class="badge badge-gold">COVID Catalyst</span>
                <span class="badge badge-blue" style="margin-left:0.4rem;">M365 Adoption</span>
                <span class="badge badge-grey" style="margin-left:0.4rem;">2,000+ Staff</span>
            </div>
        </div>

        <!-- 2021: Strategy -->
        <div class="timeline-item">
            <div class="timeline-date">2021</div>
            <div class="timeline-title">Digital Transformation Strategy Launched &mdash; Three-Front Approach</div>
            <div class="timeline-body">
                <p>ERC publishes its <strong>Digital Transformation Strategy 2021&ndash;2024</strong>, which sets out a coherent, structured approach to digital change across the council. The strategy is organised around three strategic fronts:</p>
                <ul>
                    <li><strong>Customer Experience:</strong> Improving how residents access and experience council services digitally &mdash; the MyEastRen portal, Goss CXM migration, self-service expansion</li>
                    <li><strong>Workforce Productivity:</strong> Equipping staff with digital tools and skills to work more efficiently &mdash; M365, mobile working, data analytics</li>
                    <li><strong>Business Systems:</strong> Modernising legacy back-office ICT systems across all service areas</li>
                </ul>
                <p>To deliver the strategy, the <strong>MAP (Modern, Ambitious Programme)</strong> is formally established with six departmental delivery programmes, each with their own programme manager and steering group, all governed by an overarching Programme Board. Housing Digital Transformation sits within this framework.</p>
                <span class="badge badge-blue">Strategic</span>
                <span class="badge badge-gold" style="margin-left:0.4rem;">MAP Programme</span>
                <span class="badge badge-green" style="margin-left:0.4rem;">3 Strategic Fronts</span>
            </div>
        </div>

        <!-- 2021: Digital Champions -->
        <div class="timeline-item">
            <div class="timeline-date">2021</div>
            <div class="timeline-title">Digital Champions Network &amp; SAtSD Capability</div>
            <div class="timeline-body">
                <p>ERC establishes a <strong>Digital Champions Network</strong> of 30+ staff drawn from across all departments. Champions receive specialist training and act as peer-to-peer advocates for digital adoption within their teams. This is not a token gesture &mdash; champions have a structured role in supporting colleagues through the learning of new tools and ways of working.</p>
                <p>In parallel, ERC invests in building genuine <strong>Scottish Approach to Service Design (SAtSD)</strong> capability through the Scottish Digital Academy. Over 100 staff receive training in SAtSD, user research, and agile delivery methods. This is a significant investment for a council of ERC's size and reflects a genuine commitment to user-centred design rather than technology-led IT projects.</p>
                <p>For the Housing Digital Transformation: the Digital Champions network provides ready-made change agents for the programme; the SAtSD capability means ERC already has the methodology knowledge to conduct proper tenant user research.</p>
                <span class="badge badge-blue">Workforce</span>
                <span class="badge badge-teal" style="margin-left:0.4rem;">SAtSD Capability</span>
                <span class="badge badge-grey" style="margin-left:0.4rem;">30+ Champions</span>
            </div>
        </div>

        <!-- 2021-2022: MyEastRen -->
        <div class="timeline-item">
            <div class="timeline-date">2021 &ndash; 2022</div>
            <div class="timeline-title">MyEastRen Citizen Portal &amp; mygov.scot SSO</div>
            <div class="timeline-body">
                <p>ERC launches <strong>MyEastRen</strong>, a single-sign-on citizen portal built on the national <strong>mygov.scot account</strong> infrastructure provided by the Scottish Government. Residents can log in once using their national account and access a growing range of personalised council services. This avoids the creation of yet another username and password and integrates ERC's services with the national digital identity platform.</p>
                <p>The Goss CXM platform is progressively migrating services from legacy systems onto the portal, with forms and transactions moved across in priority order. This is ongoing work through the strategy period.</p>
                <p>The relevance to Housing Digital Transformation is direct and significant: the planned Housing Repairs tenant portal will use mygov.scot SSO through the MyEastRen infrastructure. ERC does not need to build tenant identity management from scratch &mdash; it already exists.</p>
                <span class="badge badge-blue">Citizen Experience</span>
                <span class="badge badge-teal" style="margin-left:0.4rem;">mygov.scot SSO</span>
            </div>
        </div>

        <!-- 2022: IoT -->
        <div class="timeline-item">
            <div class="timeline-date">2022</div>
            <div class="timeline-title">IoT Pilots: Housing Sensors and Building Monitoring</div>
            <div class="timeline-body">
                <p>ERC takes a significant step into the Internet of Things with a housing sensor pilot. <strong>50 housing properties</strong> are fitted with <strong>humidity and temperature sensors</strong>, enabling real-time monitoring for early identification of damp and mould conditions. Rather than waiting for tenants to report problems &mdash; often after significant damage has occurred &mdash; the system alerts housing officers proactively when readings exceed safe thresholds.</p>
                <p>The pilot also extends to ERC's estate more broadly: <strong>CO2 sensors</strong> are installed in schools and council offices for ventilation and occupancy monitoring. This wider IoT programme demonstrates ERC's willingness to test new technology in a controlled, evidence-based way.</p>
                <p>For the Housing Digital Transformation: this pilot is the proof of concept for the future-state vision of proactive, sensor-driven housing maintenance across all 3,141 units. The technology, data infrastructure, and management processes have already been tested at small scale.</p>
                <span class="badge badge-green">IoT</span>
                <span class="badge badge-gold" style="margin-left:0.4rem;">Proactive Maintenance</span>
                <span class="badge badge-grey" style="margin-left:0.4rem;">50 Properties</span>
            </div>
        </div>

        <!-- 2022: Frontline Tech -->
        <div class="timeline-item">
            <div class="timeline-date">2022</div>
            <div class="timeline-title">Frontline Technology: Waste Management &amp; Roads Service</div>
            <div class="timeline-body">
                <p>ERC demonstrates that digital transformation reaches beyond office-based staff with two significant frontline technology deployments. <strong>In-cab telematics</strong> are rolled out across ERC's 25 waste management bin lorries. The system provides real-time GPS tracking, route optimisation, and automated reporting &mdash; saving the equivalent of <strong>1 day per week</strong> in administration time that was previously spent on manual reconciliation and report compilation.</p>
                <p>The <strong>Roads Service</strong> receives wearable technology for monitoring <strong>Hand-Arm Vibration Syndrome (HAVS)</strong> exposure among workers using vibratory equipment. This demonstrates ERC's willingness to use technology for staff welfare and compliance, not just efficiency.</p>
                <p>These frontline deployments are directly relevant to the Housing Repairs programme: they prove that ERC's operational workforce (not just office staff) can adopt mobile and connected technology, and that management processes can be built around real-time data. The repairs workforce deployment is a logical extension of the same approach.</p>
                <span class="badge badge-blue">Frontline Technology</span>
                <span class="badge badge-teal" style="margin-left:0.4rem;">Mobile Workforce</span>
            </div>
        </div>

        <!-- 2022-2023: Data -->
        <div class="timeline-item">
            <div class="timeline-date">2022 &ndash; 2023</div>
            <div class="timeline-title">Data Intelligence: Golden Record PoC &amp; Power BI</div>
            <div class="timeline-body">
                <p>ERC undertakes a significant <strong>&ldquo;Golden Record&rdquo; proof of concept</strong>: an exercise to match and merge data from three distinct operational systems &mdash; Housing, NHS Shielding records, and Homecare &mdash; into a single, coherent view of individuals. The goal is to understand how many people have multiple connections to council services and to enable more joined-up, proactive support.</p>
                <p>This PoC is technically complex (different data schemas, different identifiers, data quality issues) and ethically important (GDPR, data sharing agreements, consent). The lessons from this exercise directly inform the data architecture planning needed for Housing Digital Transformation, where the &ldquo;golden record&rdquo; concept needs to be applied to tenant and property data.</p>
                <p><strong>Power BI</strong> dashboards are deployed across multiple service areas for real-time demand and performance monitoring. This platform is already available and used by staff for the Benefits Realisation Dashboard planned for the Housing programme.</p>
                <span class="badge badge-blue">Data &amp; Analytics</span>
                <span class="badge badge-gold" style="margin-left:0.4rem;">Golden Record</span>
                <span class="badge badge-teal" style="margin-left:0.4rem;">Power BI</span>
            </div>
        </div>

        <!-- 2023: Health & Social Care -->
        <div class="timeline-item">
            <div class="timeline-date">2023</div>
            <div class="timeline-title">Health &amp; Social Care Digital: Telecare &amp; Virtual Visiting</div>
            <div class="timeline-body">
                <p>ERC's Health and Social Care Partnership deploys <strong>Attend Anywhere</strong> video consulting and <strong>Microsoft Teams virtual visiting</strong> for social workers and supported individuals, significantly reducing the need for some in-person visits while maintaining meaningful human contact. This is particularly important for housebound and vulnerable residents.</p>
                <p>A major strategic transformation begins: ERC commences the <strong>Digital Telecare Transition Programme</strong> to migrate <strong>3,000 residents</strong> from analogue to digital telecare devices ahead of the national analogue telephone network switch-off by 2025. This is a mandatory programme with significant implications for vulnerable residents and is being run as a carefully managed change programme with proactive resident engagement.</p>
                <p>The telecare transition is relevant to Housing because many of the affected residents are housing tenants, and the programme requires close coordination between Housing, H&amp;SCP, and Digital teams &mdash; demonstrating the cross-departmental working model that the Housing Repairs programme will also require.</p>
                <span class="badge badge-green">Health &amp; Social Care</span>
                <span class="badge badge-red" style="margin-left:0.4rem;">Analogue Switch-Off 2025</span>
                <span class="badge badge-grey" style="margin-left:0.4rem;">3,000 Residents</span>
            </div>
        </div>

        <!-- 2023-2024: Culture & Leisure -->
        <div class="timeline-item">
            <div class="timeline-date">2023 &ndash; 2024</div>
            <div class="timeline-title">Culture &amp; Leisure: Digital Touchpoints</div>
            <div class="timeline-body">
                <p>ERC's digital transformation extends into culture and leisure services, demonstrating the breadth of the MAP programme. <strong>Learn2 swim</strong> participant tracking tablets are introduced at ERC's swimming pools, allowing instructors and parents to monitor progress digitally rather than through paper records. The system integrates with the booking and payments platform.</p>
                <p>ERC libraries gain <strong>&ldquo;Open More&rdquo;</strong> out-of-hours swipe access for registered library members, extending access hours without requiring additional staffing. These deployments demonstrate ERC's commitment to applying digital solutions across all service areas, not just high-profile corporate systems.</p>
                <span class="badge badge-teal">Culture &amp; Leisure</span>
                <span class="badge badge-grey" style="margin-left:0.4rem;">Service Breadth</span>
            </div>
        </div>

        <!-- 2024: HAMP -->
        <div class="timeline-item">
            <div class="timeline-date">2024</div>
            <div class="timeline-title">Housing Asset Management Plan 2024&ndash;2026 Published</div>
            <div class="timeline-body">
                <p>ERC publishes the <strong>Housing Asset Management Plan (HAMP) 2024&ndash;2026</strong>, a strategic document covering the future of ERC's social housing stock of <strong>3,141 units</strong>. Key commitments include:</p>
                <ul>
                    <li>EPC Band B target by 2032 (Scottish Government social housing energy standard)</li>
                    <li>171 new housing units to be completed by 2028 (new build programme)</li>
                    <li>Planned maintenance investment priorities for the next two years</li>
                    <li><strong>Digital Repairs project formally scoped</strong> within the plan as a key enabling investment for improving repairs productivity and tenant experience</li>
                </ul>
                <p>The HAMP provides the business case context for the Housing Digital Transformation role. It confirms that the Digital Repairs project is a formally approved, strategically aligned investment &mdash; not a speculative idea &mdash; and that it sits within a broader housing management modernisation agenda.</p>
                <span class="badge badge-blue">Strategic</span>
                <span class="badge badge-gold" style="margin-left:0.4rem;">HAMP 2024&ndash;26</span>
                <span class="badge badge-green" style="margin-left:0.4rem;">Digital Repairs Scoped</span>
            </div>
        </div>

        <!-- 2024-2026: Horizon -->
        <div class="timeline-item">
            <div class="timeline-date">2024 &ndash; 2026 &amp; Beyond</div>
            <div class="timeline-title">Next Horizon: Housing Digital Transformation &amp; System Modernisation</div>
            <div class="timeline-body">
                <p>ERC's digital transformation enters its most ambitious phase. Key programmes on the horizon:</p>
                <ul>
                    <li><strong>Housing Repairs Digital Transformation (this role):</strong> 20-month programme to deliver tenant self-service portal, mobile workforce management, scheduling optimisation, and productivity improvements for the repairs service</li>
                    <li><strong>Legacy system replacement:</strong> The Housing management system, SEEMIS (education), and CareFirst (social care) are all identified for replacement or major modernisation &mdash; the most complex and highest-risk element of the transformation programme</li>
                    <li><strong>Unified Communications:</strong> Consolidation of telephony and digital communication channels into a single platform integrated with Goss CXM</li>
                    <li><strong>AI and chatbot exploration:</strong> ERC begins exploring the use of artificial intelligence for customer service automation, demand forecasting, and decision support &mdash; building on the Microsoft Azure AI capabilities available within the M365 subscription</li>
                    <li><strong>IoT expansion:</strong> Scale-up of the housing sensor pilot from 50 to potentially all 3,141 units for proactive damp and mould management</li>
                </ul>
